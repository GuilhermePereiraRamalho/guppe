"""
O tipo de dado None  em Python, representa o tipo sem tipo, ou poderia ser conhecido também como tipo vazio, porém, falar que é um tipo sem tipo é mais apropriado

OBS: O tipo None é SEMPRE representaod com a primeira letra maiúscula.

Certo: None
Errado: none

Quando utilizamos?
    - Podemos utilizar None quando queremos criar uma variável e inicializá-la com um tipo sem tipo, antes de recebermos um falor final

OBS: O tipo None em Python é sempre considerado como falso
"""
numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.34
print(numeros)
print(type(numeros))
